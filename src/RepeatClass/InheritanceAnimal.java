 package RepeatClass;

public class InheritanceAnimal {
	
	String name, color;
	int age, weight;
	
	public void eat() {
		System.out.println("All animals eat");
	}
	public void sleep() {
		System.out.println("All animals sleep");
	}
	public void displayInfo() {
		System.out.println("name: "+ name + " color: "+ color + " age: "+ age + " weight: "+ weight);
	}
	
	
 }
